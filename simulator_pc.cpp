static size_t reading_index = 0;

static double read_simulator() { return readings[reading_index++]; }
template <int const len_zeroes, int const len_poles>
struct IIRCascadeSimulator
    : public IIRCascadeController<len_zeroes, len_poles> {
  static_assert(len_zeroes < len_poles,
                "There should be more poles than zeroes. ");

  func_t output_to_input;

  IIRCascadeSimulator(read_func_t reader, write_func_t writer,
                      double const (&zeroes)[len_zeroes],
                      double const (&poles)[len_poles],
                      double const overall_gain, double const lower,
                      double const upper, func_t output_to_input)
      : IIRCascadeController<len_zeroes, len_poles>(
            reader, writer, zeroes, poles, overall_gain, lower, upper),
        output_to_input(output_to_input) {}

    double last_error; 
  void update() override {
    double new_out = this->reader() + output_to_input(this->last_out);
    this -> last_error = new_out; 
    for (auto p : this->controllers)
      new_out = p->transfer(new_out);
    new_out = max(min(this->overall_gain * new_out, this->upper), this->lower);
    this->last_out = (int)new_out;
  }
};

template <int len_zeroes, int len_poles>
IIRCascadeSimulator<len_zeroes, len_poles> make_iir_cascade_simulator(
    double const (&zeroes)[len_zeroes], double const (&poles)[len_poles],
    double const overall_gain, func_t output_to_input,
    double const lower = -32768., double const upper = 32767.) {
  return IIRCascadeSimulator<len_zeroes, len_poles>(
      read_simulator, [](double x) {}, zeroes, poles, overall_gain, lower,
      upper, output_to_input);
}

